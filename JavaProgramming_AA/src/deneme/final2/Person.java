package deneme.final2;

public class Person {
    private String name;
    private final int age;

    public Person() {
        this.age = 15;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }


}
