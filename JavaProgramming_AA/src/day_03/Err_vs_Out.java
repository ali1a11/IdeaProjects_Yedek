package day_03;
/*
 System.err.println(): used for printing error messages (red color)

 System.out.println(): regular outputs (black color)

 */
public class Err_vs_Out {
    public static void main(String[] args) {
        System.out.println("Hello World"); // regular output black color
        System.out.println();
        System.err.println("Hello World"); // error mesaj shortcut serr + Enter
    }
}
