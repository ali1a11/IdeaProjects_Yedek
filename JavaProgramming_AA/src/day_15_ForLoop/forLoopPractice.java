package day_15_ForLoop;

public class forLoopPractice {
    public static void main(String[] args) {
        for (int i = 15; i <= 45; i++) {
            System.out.print(i + " ");

        }
        System.out.println();
        System.out.println("Hello");

        //backward

        for (int i =100; i>=50; i--){
            System.out.print(i + " ");
        }
        System.out.println();
        System.out.println("Hello");
    }
}
