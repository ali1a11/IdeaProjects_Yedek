package from_Short_Videos;

public class PrimitiveCasting {
    public static void main(String[] args) {
        // implicit casting
        int a = 100;
        double b = a;

        System.out.println("a = " + a);
        System.out.println("b = " + b);

        // explicit casting
        double x = 20.5;
        int y = (int)x;

        System.out.println("x = " + x);
        System.out.println("y = " + y);

    }
}
