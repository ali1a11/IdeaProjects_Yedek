package from_Short_Videos;

public class Method_Custom_Void {
    public static void main(String[] args) {
        displayMessage();

    }

    // access modifier spacifier return type methodName parenthesis
    public static void displayMessage() {
        System.out.println("MESSAGE:");
        System.out.println("Hello World!!!");
    }

    // Static modifier allows us to call the method through the class name
    // no method inside another method

}
