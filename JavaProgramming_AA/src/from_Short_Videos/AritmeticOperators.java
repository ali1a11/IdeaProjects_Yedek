package from_Short_Videos;

public class AritmeticOperators {
    public static void main(String[] args) {
        System.out.println(1000 + 1250); // Addition +

        System.out.println(100 - 25); // substraction -

        System.out.println(4 * 2.5); // multiplication *

        System.out.println(10/4); // 2 // int/int = int

        System.out.println(10.0/4); // 2.5

        System.out.println(10/4.0); // 2.5

        System.out.println(20/6); // 3

        System.out.println(20/6D); // 3.3333333333333335

        System.out.println(10%4); // 2 // remainder %



    }

}
