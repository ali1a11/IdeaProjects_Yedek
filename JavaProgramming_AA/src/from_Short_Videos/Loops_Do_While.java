package from_Short_Videos;

public class Loops_Do_While {
    public static void main(String[] args) {

        int num = 0;

        do {
            System.out.println("Hello"); // Even the condition is false statement will be executed once (before while this line prints)
        } while (num > 5);


        int num2 = 0;

        do {
            System.out.println("Hello 222"); // before while this line prints
            num2++;
        } while (num2 < 4);



    }
}
