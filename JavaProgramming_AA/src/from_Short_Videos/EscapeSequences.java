package from_Short_Videos;

public class EscapeSequences {
    public static void main(String[] args) {
        System.out.println("Hello world!\nHow are you?");
        System.out.println("\tJava is fun.");
        System.out.println("\\"); // prints \
        System.out.println("His favorite programming language is \"Java\""); //His favorite programming language is "Java"
    }
}
