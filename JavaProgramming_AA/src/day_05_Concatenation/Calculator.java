package day_05_Concatenation;

public class Calculator {
    public static void main(String[] args) {
        int firstNumber = 100;
        int secondNumber = 50;

        System.out.println(firstNumber + " +  " + secondNumber + " = " + (firstNumber+secondNumber));
        System.out.println(firstNumber + " -  " + secondNumber + " = " + (firstNumber-secondNumber));
        System.out.println(firstNumber + " *  " + secondNumber + " = " + (firstNumber*secondNumber));
        System.out.println(firstNumber + " /  " + secondNumber + " = " + (firstNumber/secondNumber));
    }
}
