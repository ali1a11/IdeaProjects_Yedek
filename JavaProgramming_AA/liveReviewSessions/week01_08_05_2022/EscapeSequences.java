package week01_08_05_2022;

public class EscapeSequences {
    /*
    \t  for tab
    \n  for new line
    \\  for \
    \"  for "
     */
    public static void main(String[] args) {
        System.out.println("Try\nNext line");
        System.out.println("Tab\ttab\t\ttabtab");
        System.out.println("Write\\");
        System.out.println("Write \"let everybody code\"");
    }
}
